#include "ft_printf.h"

int	ft_nbr(int nbr)
{
	int	x;

	x = 0;
	if (nbr == -2147483648)
	{
		x += ft_str("-2147483648");
		return (x);
	}
	if (nbr < 0)
	{
		nbr = -nbr;
		x += ft_char('-');
	}
	if (nbr > 9)
	{
		x += ft_nbr(nbr / 10);
	}
	x += ft_char((nbr % 10) + '0');
	return (x);
}
