#include "ft_printf.h"

int	ft_str(char *str)
{
	int	x;

	x = 0;
	if (!str)
	{
		x += ft_str("(null)");
		return (x);
	}
	while (str[x])
	{
		ft_char(str[x]);
		x++;
	}
	return (x);
}
